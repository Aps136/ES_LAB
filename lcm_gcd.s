	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler 
	ALIGN 
	AREA mycode,CODE ,READONLY                                          
	ENTRY
	EXPORT Reset_Handler
Reset_Handler	
	LDR R0, =NUM1
	LDR R1, =NUM2
	LDR R2, [R0]
	LDR R3, [R1]	
UP CMP R2, R3
	BEQ EXIT
	SUBHI R2, R3
	SUBLO R3, R2
	B UP
EXIT LDR R4, =LCM
	STR R2, [R4]; R2 HAS GCD
	LDR R0, =NUM1
	LDR R0, [R0]
	LDR R1, =NUM2
	LDR R1, [R1]
	
	MUL R6, R0, R1; R6 HAS A*B
	UDIV R7, R6, R2; R7 HAS ANSWER
	LDR R8, =RESULT
	STR R7, [R8]
	
STOP B STOP	
NUM1 DCD 0X00000008
NUM2 DCD 0X00000002
	AREA mydata, DATA, READWRITE
LCM  DCD 0X0
RESULT DCD 0X0
	END
