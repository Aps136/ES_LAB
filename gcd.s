	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =NUM1;
	LDR R1, =NUM2;
	LDR R2, [R0]
	LDR R3, [R1]
UP CMP R2, R3
	BEQ DONE
	SUBHI R2, R3
	SUBLO R3, R2
	B UP
DONE LDR R4, =GCD;loads address of LCM
	STR R2, [R4]
	
STOP B STOP
NUM1 DCD 0X00000008
NUM2 DCD 0X00000002
	AREA mydata, DATA, READWRITE
GCD DCD 0X0
	END
