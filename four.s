	MOV R0, #10
	MOV R1, #0
	LDR R3, =N
LOOP LDR R4, [R3], #4
	ADD R1, R4
	SUBS R0, #1
	BNE LOOP
	LDR R5, =DST
	STR R1, [R5], #4
STOP B STOP
N DCD 0X1000000, 0X20000000, 0X30000000, 0X40000000, 0X50000000, 0X60000000, 0X70000000, 0X80, 0X90, 0X10
